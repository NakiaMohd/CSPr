public class Counting{
	public static void main(String args[]){
		String s1="1010100 1101000 1100101 0100000 1100011 1100001 1110100 1110011 0100000 1100100 1101001 1100100 0100000 1101110 1101111 1110100 0100000 1110011 1101001 1110100 0100000 1101111 1101110 0100000 1110100 1101000 1100101 0100000 1101101 1100001 1110100 ";
		int c=0;
		for(int x=0; x<s1.length(); x++)
		{
			if((s1.charAt(x)=='0')||(s1.charAt(x)=='1'))
			{
				c++;
			}
		}
		System.out.print(c);
	}
}